import Foundation

class Calculator {
    
    static func multiplyNum(_ firstNum: Double, _ secondNum: Double) -> Double { firstNum * secondNum }
    
    static func divideNum(_ firstNum: Double, _ secondNum: Double) -> Double { firstNum / secondNum }
    
    static func addNum(_ firstNum: Double, _ secondNum: Double) -> Double { firstNum + secondNum }
    
    static func subNum(_ firstNum: Double, _ secondNum: Double) -> Double { firstNum - secondNum }
}
